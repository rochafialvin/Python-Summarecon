# SOLVE IT - 4

# Andi + Budi = 49
# Rasio = 0.4 = 4 / 10 = 2 / 5 = 2 : 5
# Total Perbandingan = 2 + 5 = 7
# berapa usia masing - masing setelah dua tahun

totalUmur = 49
rasioAndi = 2
rasioBudi = 5
totalRasio = rasioAndi + rasioBudi

# Cari tahu umur Andi, kemudian tambah 2
umurAndi = totalUmur * (rasioAndi / totalRasio) + 2

# Cari tahu umur budi, tambah 2
umurBudi = totalUmur * (rasioBudi / totalRasio) + 2

print(f"Umur Andi adalah {umurAndi} tahun")
print(f"Umur Budi adalah {umurBudi} tahun")
